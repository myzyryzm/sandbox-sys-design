export default function serviceLb() {
  const json = (res, code, b) => {
    res.statusCode = code
    res.setHeader('Content-Type', 'application/json')
    res.end(JSON.stringify(b))
  }
  return {
    name: 'service-lb',
    configureServer(server) {
      server.middlewares.use('/api/service-lb', async (req, res, next) => {
        try {
          const url = new URL(req.url, 'http://localhost')
          if (url.pathname === '/' || url.pathname === '') {
            if (req.method === 'GET') {
              const system = url.searchParams.get('system')
              if (!isValidSystem(system)) throw bad(`unknown system "${system}"`)
              return json(res, 200, listGroups(system))
            }
            if (req.method === 'POST') {
              return json(res, 200, await setLoadBalancing(await readJsonBody(req)))
            }
          }
          return next()
        } catch (err) {
          return json(res, err.statusCode || 500, { ok: false, error: err.message })
        }
      })
    },
  }
}
